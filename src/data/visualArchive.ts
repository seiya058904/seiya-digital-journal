export type VisualArchiveCategory = 'editorial' | 'memory' | 'illustration' | 'art' | 'design'

export type VisualArchiveCity = 'Chongqing' | 'Chengdu' | 'Wuhan' | 'Kaifeng' | 'Tianjin' | 'Emeishan' | 'Venice' | null

export type VisualArchiveItem = {
  id: string
  category: VisualArchiveCategory
  city: VisualArchiveCity
  title: string
  caption: string
  note: string
  image: string
  thumb: string
  mediaType: 'image'
  aspect: 'portrait' | 'landscape' | 'square'
  tags: string[]
  featured: boolean
  alt: string
}

const base = import.meta.env?.BASE_URL ?? '/'

/** Resolve an archive image path for use in src attributes — handles BASE_URL on GitHub Pages */
export function archiveImageSrc(image: string): string {
  return `${base}${image.replace(/^\//, '')}`
}

/** Convenience: get an item's full image src */
export function itemImageSrc(item: VisualArchiveItem): string {
  return archiveImageSrc(item.image)
}

/** Convenience: get an item's full thumbnail src */
export function itemThumbSrc(item: VisualArchiveItem): string {
  return archiveImageSrc(item.thumb)
}

export function getArchiveCardImageSources(
  item: Pick<VisualArchiveItem, 'image' | 'thumb'>,
) {
  const src = archiveImageSrc(item.thumb)
  return {
    src,
    srcSet: `${src} 1x, ${archiveImageSrc(item.image)} 2x`,
  }
}

